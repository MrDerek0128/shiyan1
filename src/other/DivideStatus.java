package other;

public class DivideStatus {
    int totalSum, leftSum, rightSum, maxSum, left, right;

    public DivideStatus(int totalSum, int leftSum, int rightSum, int maxSum, int left, int right) {
        this.totalSum = totalSum;
        this.leftSum = leftSum;
        this.rightSum = rightSum;
        this.maxSum = maxSum;
        this.left = left;
        this.right = right;
    }
}
